int detect_tar_output(char *path)
{
        char buf[2048];
        char *str;
        int spaces=1,coun=1, i=0;
        if(create_file(path,"tartest.tar",tartest_chr,tartest_chr_size)==0) return 0;
        sprintf(buf,"tar tvf %s/tartest.tar",path);
        FILE *fp=popen(buf,"r");
        if(fp==NULL)
                return 1;
        fgets(buf,1024,fp);
        pclose(fp);
        while(buf[i]!=0)
        {
           if(buf[i]==' ')
           {
             spaces++;
             while(buf[i]==' ')        i++;
           } else
                   i++;
        }
        str=strstr(buf,"278");
        while(str!=buf)
        {
          if(*str==' ')
          {
                  coun++;
                  while(*str==' ')
                          str--;
          } else
                  str--;
        }
//        fprintf(stderr,"Tar test output: %d / %d words\n", spaces, coun);
        return spaces+coun*10;
}
       
